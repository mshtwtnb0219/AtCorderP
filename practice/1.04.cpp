#include <bits/stdc++.h>
using namespace std;

int main() {
  // 一年の秒数
  int seconds = 365 * 24 * 60 * 60;

  int a = seconds * 1;
  int b = seconds * 2;
  int c = seconds * 5;
  int d = seconds * 10;

  // 以下のコメント/* */を消して追記する
  cout <<  a<< endl;
  cout << b<< endl;
  cout << c<< endl;
  cout << d<< endl;
}
